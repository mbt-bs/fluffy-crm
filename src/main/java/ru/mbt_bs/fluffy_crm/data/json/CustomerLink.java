package ru.mbt_bs.fluffy_crm.data.json;

public class CustomerLink {
    private Long id;
    private String name;

    public CustomerLink() {
    }

    public CustomerLink(Long id) {
        this.id = id;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return id + " : " + name;
    }
}
